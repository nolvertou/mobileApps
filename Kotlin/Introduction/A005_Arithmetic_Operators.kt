fun main(){
	// Arithmetic Operators (+, -, *, /, %)
	// Addition
	var a = 5 + 3		
    // Result
    println("a = $a")	// 8
    
    // Substraction
	var b = 5 - 3		
    // Result
    println("b = $b")	// 2
    
    // Multiplication
	var c = 5 * 3		
    // Result
    println("c = $c")	// 15
    
    // Division 1
	var d = 5 / 3		
    // Result
    println("d = $d")	// 1
    
    
    // Division 2
	var e = 6 / 3		
    // Result
    println("e = $e")	// 2
    
    // Residue
	var f = 6 % 3		
    // Result
    println("f = $f")	// 0
    
    // Residue
	var g = 6 % 4		
    // Result
    println("g = $g")	// 2
    
}
