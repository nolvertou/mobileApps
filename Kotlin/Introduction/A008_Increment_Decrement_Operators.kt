fun main(){
    
    // Increment and Decrement Operators    
    
    var myNum = 0
    
    myNum++		// 1
    myNum++		// 2
    myNum++		// 3
    ++myNum
    
    println("myNum = $myNum") 	// 4
    
    myNum-- 		
   	println("myNum = $myNum")	// 3
    --myNum
    println("myNum = $myNum")	// 2
}
